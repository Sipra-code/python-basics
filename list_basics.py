# Creating a list
numbers = [10, 20, 30, 40, 50]

# Printing the whole list
print("Numbers:", numbers)

# Accessing elements
print("First element:", numbers[0])
print("Last element:", numbers[-1])

# Length of list
print("Total elements:", len(numbers))

# Looping through the list
print("All elements:")
for num in numbers:
    print(num)
